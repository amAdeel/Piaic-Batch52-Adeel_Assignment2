"use strict";
//Exercise 4:   Write a program that checks if a given number is even or odd.
let nm1 = [34, 56, 78, 99, 98, 65, 3433, 31];
for (let number of nm1) {
    if (number % 2 == 0) {
        console.log("Number is even");
    }
    else if (number / 2) {
        console.log("this is a odd number");
    }
}
